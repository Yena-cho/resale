(
  ID CHAR(10) PRIMARY KEY,
  TYPE CHAR(6),
  STATUS CHAR(6) DEFAULT 'A00001' NOT NULL,
  FILE_ID CHAR(10),
  CREATE_DATE DATE,
  CREATE_USER VARCHAR2(100)
);
