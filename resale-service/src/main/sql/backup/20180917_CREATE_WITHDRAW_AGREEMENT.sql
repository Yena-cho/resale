COMMENT ON COLUMN WITHDRAW_AGREEMENT.TYPE IS '출금동의 형태 (WAXXXX)';
COMMENT ON COLUMN WITHDRAW_AGREEMENT.STATUS IS '동의서 상태 (A0XXXX)';
COMMENT ON COLUMN WITHDRAW_AGREEMENT.FILE_ID IS '파일 아이디';
COMMENT ON COLUMN WITHDRAW_AGREEMENT.CREATE_DATE IS '등록일시';
COMMENT ON COLUMN WITHDRAW_AGREEMENT.CREATE_USER IS '등록자';
COMMENT ON TABLE WITHDRAW_AGREEMENT IS '출금동의서';

